    <p>VALORES: Liberdade econômica e social, negação de autoritarismo social, econômico, político e ideológico (nulius in verba). Compromisso com a realidade factual, com a precisão e acurácia dos fatos. Seriedade. Negação de violência. Cordialidade, ponderação e educação.</p>

    <p>PARADIGMAS: Valorização de estratégias positivas ao invés de negativas. Relacionamento de ideias com fatos reais. Releitura histórica sob a perspectiva da liberdade.</p>

    <p>VISÃO: Ideias e apenas ideias podem iluminar a escuridão, e assim temos como visão a disseminação delas para que gradualmente as pessoas se concientizem de novas possibilidades de auto-organização.</p>

    <p>TERMOS: Não usar termos raivosos e agressivos. Cuidado ao usar o termo "público" e dar preferência a termos mais adequados como "estatal". Prezar pelo correto uso da língua.</p>

    <p>HIERARQUIA DE TEMAS: Estão no topo: Política e Economia seguido dos demais temas relacionados diretamente a liberdade como questões sociais e anarquia epistemológica.</p>


