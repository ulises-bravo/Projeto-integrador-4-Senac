                    <h1>O instituto</h1>
                    <span>
                        <p>A não conformidade, a busca pelo ilimitado e a paixão pela liberdade fez com que em 2017 alguns indivíduos se reunissem e espontaneamente formassem um grupo. Ainda sem uma direção muito fixa, este grupo começou a experimentar vários rumos de divulgação das ideias de liberdade que acabaram culminando no primeiro curso brasileiro de libertarianismo. Este curso foi de grande sucesso e teve a presença de grandes nomes como o Raphaël Lima, Alexis Efremides, Thiago Ávila e os professores Ronald Hillbrecht e Carlo Requião. 

Este mesmo grupo que até então se auto denominava Liber decidiu voar mais alto, tentar acrobacias mais arriscadas e devolver o aprendizado para todos. Assim, inspirado no romance de Fernão Capelo Gaivota, este grupo em 2018 tornou-se o Instituto Libertat. 

O Instituto Libertat é hoje um porta-voz das ideias mais profundas de liberdade, sempre tendo como pilar principal o princípio da auto-propriedade. Assim, disseminamos ideias relacionadas a diverentes vertentes como o libertarianismo e o voluntarismo. Em seu estatuto o Instituto Libertat se apresenta como um think-thank que não aceita contribuições estatais e de partidos políticos.</p>
