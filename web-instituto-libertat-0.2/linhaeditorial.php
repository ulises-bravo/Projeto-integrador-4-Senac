    <h2>Valores</h2>
	<p>Liberdade econômica e social, negação de autoritarismo social, econômico, político e ideológico (nulius in verba). Compromisso com a realidade factual, com a precisão e acurácia dos fatos. Seriedade. Negação de violência. Cordialidade, ponderação e educação.</p>

    <h2>Paradigmas</h2>
	<p>Valorização de estratégias positivas ao invés de negativas. Relacionamento de ideias com fatos reais. Releitura histórica sob a perspectiva da liberdade.</p>

    <h2>Visão</h2>
	<p>Ideias e apenas ideias podem iluminar a escuridão, e assim temos como visão a disseminação delas para que gradualmente as pessoas se concientizem de novas possibilidades de auto-organização.</p>

    <h2>Termos</h2>
	<p>Não usar termos raivosos e agressivos. Cuidado ao usar o termo "público" e dar preferência a termos mais adequados como "estatal". Prezar pelo correto uso da língua.</p>

    <h2>Hierarquia de Temas</h2> 
	<p>Estão no topo: Política e Economia seguido dos demais temas relacionados diretamente a liberdade como questões sociais e anarquia epistemológica.</p>

